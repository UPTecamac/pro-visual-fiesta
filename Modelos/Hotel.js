class Hotel{
    nombre = 'HOla Juanito'
    total = 0
    constructor(x, y)
    {
        this.x = x
        this.y = y
    }

    sumar()
    {
        return this.x + this.y
    }

    restar()
    {
        return this.x - this.y
    }

    multiplicar()
    {
        return this.x * this.y
    }

}
